function changePlace(elem){

    if (elem = 'candyking'){
        window.location.href = "../Concert/concert.html"; 
    }

    if (elem = 'gummybear'){
        window.location.href = "../Minecraft/minecraft.html"; 
    }

    if (elem = 'gummybear2'){
        window.location.href = "../Snow/snow.html"; 
    }

    if (elem = 'gummybear3'){
        window.location.href = "../Final_Main_Page/index.html"; 
    }

    if (elem = 'gummybear4'){
        window.location.href = "../Trees/index.html"; 
    }

    if (elem = 'gummybear5'){
        window.location.href = "../Roller_Coaster/ride.html"; 
    }

    if (elem = 'bee'){
        window.location.href = "../Sewer/sewer.html";
    }

}